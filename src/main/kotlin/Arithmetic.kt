fun main() {
    var value1: Int = 10;
    var value2: Int = 20;

    // Math operator
    val tambah: Int = value1 + value2;
    val kurang: Int = value1 + value2;
    val kali: Int = value1 * value2;
    val bagi: Int = value1 / value2;

    println(tambah);
    println(kurang);
    println(bagi);
    println(kali);

    // Augmented Operator
    var total: Int = 0;
    val value3: Int = 5;
    val value4: Int = 8;

    total += value3;
    total *= value4;

    println(total);
}